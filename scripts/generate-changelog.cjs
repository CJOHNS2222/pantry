#!/usr/bin/env node
/**
 * generate-changelog.cjs
 * Parses CHANGELOG.md and writes constants/changelogEntries.ts
 * Runs automatically via the `prebuild` and `predev` npm hooks.
 *
 * Changelog format expected:
 *   ## [X.Y.Z] - YYYY-MM-DD
 *   ### Added / Fixed / Changed
 *   - **Title** — short description
 */
'use strict';

const fs = require('fs');
const path = require('path');

const CHANGELOG_PATH = path.resolve(__dirname, '../CHANGELOG.md');
const OUTPUT_PATH = path.resolve(__dirname, '../constants/changelogEntries.ts');

/** How many recent versions to include in the modal. */
const MAX_VERSIONS = 3;
/** Max highlight bullets per version. */
const MAX_HIGHLIGHTS = 4;

const MONTHS = ['Jan', 'Feb', 'Mar', 'Apr', 'May', 'Jun',
                'Jul', 'Aug', 'Sep', 'Oct', 'Nov', 'Dec'];

function formatDate(iso) {
  const [y, m, d] = iso.split('-').map(Number);
  return `${MONTHS[m - 1]} ${d}, ${y}`;
}

function parseChangelog(content) {
  // Split on version-header boundaries; first element is empty/preamble
  const blocks = content.split(/^## \[/m).slice(1);
  const entries = [];

  for (const block of blocks.slice(0, MAX_VERSIONS)) {
    const headerMatch = block.match(/^([\d.]+)\] - (\d{4}-\d{2}-\d{2})/);
    if (!headerMatch) continue;

    const version = headerMatch[1];
    const date = formatDate(headerMatch[2]);

    // Extract highlights from `- **Bold Title** — description` lines
    const highlights = [];
    for (const line of block.split('\n')) {
      if (highlights.length >= MAX_HIGHLIGHTS) break;
      // Allow em-dash (—), en-dash (–), or hyphen-space as the separator
      const m = line.match(/^- \*\*(.+?)\*\*(?:\s*[—–]\s*(.+))?/);
      if (!m) continue;

      const title = m[1].trim();
      const rawDesc = m[2] ? m[2].trim() : '';
      // First try to end at a sentence period; fall back to the whole rawDesc
      const periodIdx = rawDesc.search(/\.\s/);
      const sentence = periodIdx !== -1 ? rawDesc.slice(0, periodIdx + 1) : rawDesc;
      // Strip inline code backticks for cleaner modal display
      const cleaned = sentence.replace(/`[^`]+`/g, (s) => s.slice(1, -1));
      const text = cleaned ? `${title} — ${cleaned}` : title;
      // Truncate at word boundary within 120 chars
      let highlight = text;
      if (text.length > 120) {
        const cut = text.slice(0, 120).replace(/\s\S*$/, '');
        highlight = cut + '…';
      }
      highlights.push(highlight);
    }

    // Skip versions that have no bold-titled bullets (e.g. pure Changed entries)
    if (highlights.length > 0) {
      entries.push({ version, date, highlights });
    }
  }

  return entries;
}

/**
 * Parses all versions in detail, splitting by section (Added, Changed, Fixed)
 * and extracting all bullet points for the comprehensive ChangelogPage.
 */
function parseDetailedChangelog(content) {
  const blocks = content.split(/^## \[/m).slice(1);
  const entries = [];

  for (const block of blocks) {
    const headerMatch = block.match(/^([\d.]+)\] - (\d{4}-\d{2}-\d{2})/);
    if (!headerMatch) continue;

    const version = headerMatch[1];
    const date = formatDate(headerMatch[2]);

    const sections = [];
    // Split block by "### " to get sections (Added, Changed, Fixed, etc.)
    const sectionParts = block.split(/^### /m).slice(1);
    
    for (const part of sectionParts) {
      const lines = part.split('\n');
      const title = lines[0].trim();
      const bullets = [];
      
      for (let i = 1; i < lines.length; i++) {
        const line = lines[i].trim();
        if (line.startsWith('- ')) {
          const bulletText = line.slice(2).trim();
          if (bulletText) {
            bullets.push(bulletText);
          }
        }
      }
      
      if (bullets.length > 0) {
        sections.push({ title, bullets });
      }
    }

    if (sections.length > 0) {
      entries.push({ version, date, sections });
    }
  }

  return entries;
}

// ── Main ──────────────────────────────────────────────────────────────────────

let content;
try {
  content = fs.readFileSync(CHANGELOG_PATH, 'utf8');
} catch (err) {
  console.error(`generate-changelog: could not read ${CHANGELOG_PATH}`, err.message);
  process.exit(1);
}

const entries = parseChangelog(content);
const detailedEntries = parseDetailedChangelog(content);

if (entries.length === 0) {
  console.warn('generate-changelog: no entries parsed — check CHANGELOG.md format');
}

const output = `\
// AUTO-GENERATED by scripts/generate-changelog.cjs — do not edit manually.
// Regenerates on every \`npm run build\` and \`npm run dev\` via npm lifecycle hooks.

export interface ChangeEntry {
  version: string;
  date: string;
  highlights: string[];
}

export interface DetailedSection {
  title: string;
  bullets: string[];
}

export interface DetailedChangeEntry {
  version: string;
  date: string;
  sections: DetailedSection[];
}

export const RECENT_CHANGES: ChangeEntry[] = ${JSON.stringify(entries, null, 2)};

export const ALL_CHANGES: DetailedChangeEntry[] = ${JSON.stringify(detailedEntries, null, 2)};
`;

fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
fs.writeFileSync(OUTPUT_PATH, output, 'utf8');
console.log(`✅  changelogEntries.ts generated (${entries.length} recent, ${detailedEntries.length} total versions)`);
