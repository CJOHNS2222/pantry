#!/usr/bin/env node
'use strict';
/**
 * cleanup-placeholder-images.cjs
 * Deletes Spoonacular food-cloche placeholder images that were incorrectly
 * downloaded, and regenerates data/item-images.ts without them.
 */
const crypto = require('crypto');
const fs = require('fs');
const path = require('path');

const PLACEHOLDER_MD5 = 'e5e28152a2797ed1c3ffb6e2db37423c'; // food cloche icon
const IMAGES_DIR = path.join(__dirname, '..', 'public', 'images', 'items');
const PROGRESS_FILE = path.join(__dirname, '.image-seed-progress.json');
const TS_OUTPUT = path.join(__dirname, '..', 'data', 'item-images.ts');
const CDN_SIZE = '500x500';

// 1. Find and delete all placeholder files
const files = fs.readdirSync(IMAGES_DIR);
const deletedFiles = new Set();
for (const file of files) {
  const buf = fs.readFileSync(path.join(IMAGES_DIR, file));
  const hash = crypto.createHash('md5').update(buf).digest('hex');
  if (hash === PLACEHOLDER_MD5) {
    fs.unlinkSync(path.join(IMAGES_DIR, file));
    deletedFiles.add(file);
  }
}
console.log('Deleted', deletedFiles.size, 'placeholder files');

// 2. Remove those entries from progress mapping
const progress = JSON.parse(fs.readFileSync(PROGRESS_FILE, 'utf8'));
const cleaned = {};
let removed = 0;
for (const [key, filename] of Object.entries(progress.mapping)) {
  if (deletedFiles.has(filename)) {
    removed++;
  } else {
    cleaned[key] = filename;
  }
}
console.log('Removed', removed, 'mapping entries');
console.log('Remaining good entries:', Object.keys(cleaned).length);
progress.mapping = cleaned;
fs.writeFileSync(PROGRESS_FILE, JSON.stringify(progress, null, 2));

// 3. Regenerate data/item-images.ts
const entries = Object.entries(cleaned)
  .sort(([a], [b]) => a.localeCompare(b))
  .map(([k, v]) => '  ' + JSON.stringify(k) + ': ' + JSON.stringify(v) + ',')
  .join('\n');

const ts = [
  '/**',
  ' * AUTO-GENERATED by scripts/seed-ingredient-images.cjs',
  ' * DO NOT EDIT MANUALLY -- run `npm run seed:images` to regenerate.',
  ' *',
  ' * Maps normalized item names => Spoonacular ingredient image filenames.',
  ' *',
  ' * Resolution order in getItemImage():',
  ' *   1. Manual curated mappings (existing PNGs in /images/)',
  ' *   2. This map => /images/items/{filename}  (locally downloaded, offline-first)',
  ' *   3. Spoonacular CDN URL fallback via onError handler',
  ' *   4. Category fallback',
  ' *   5. /images/placeholder.svg  (triggers async fetchExternalItemImage)',
  ' *',
  ' * To rebuild: npm run seed:images',
  ' * Generated: ' + new Date().toISOString(),
  ' * Items: ' + Object.keys(cleaned).length,
  ' */',
  'export const itemImages: Record<string, string> = {',
  entries,
  '};',
  '',
  '/** CDN base URL for fallback when local image is unavailable (fresh clone etc.) */',
  "export const ITEM_IMAGE_CDN_BASE = 'https://spoonacular.com/cdn/ingredients_" + CDN_SIZE + "/';",
  '',
].join('\n');

fs.writeFileSync(TS_OUTPUT, ts);
console.log('Regenerated', TS_OUTPUT);
